var Texture = require('Texture');

// size of characters in the font tilesheet
var TILE_WIDTH  = 8;
var TILE_HEIGHT = 8;
var CHAR_OFFSET = 32;

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
function TextBox(w, h, font, color) {
	this.w     = w;
	this.h     = h;
	this.paper = 0;
	this.color = 0;

	this.texture = new Texture(w, h);
	// this.texture.setTilesheet(font);
}
module.exports = TextBox;

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
TextBox.prototype.clear = function () {
	this.texture.clear();
	return this;
};

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
TextBox.prototype.cls = function () {
	this.texture.paper(this.paper);
	this.texture.cls();
	return this;
};

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
TextBox.prototype.draw = function (x, y) {
	draw(this.texture, x, y);
};

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
TextBox.prototype.addText = function (text, x, y) {
	for (var t = 0; t < text.length; t++) {
		var c = text.charCodeAt(t);

		// character sprite
		var s = c - CHAR_OFFSET;

		// location of the character in the tilesheet
		var i = s % 16;
		var j = ~~(s / 16);

		// drawing
		this.texture.ctx.drawImage(assets.font, i * TILE_WIDTH, j * TILE_HEIGHT, TILE_WIDTH, TILE_HEIGHT, x, y, TILE_WIDTH, TILE_HEIGHT);
		x += TILE_WIDTH;
	}
	return this;
};

